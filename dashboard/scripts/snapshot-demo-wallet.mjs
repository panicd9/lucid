// One-shot snapshot of the localnet `treasury` wallet — captures raw account
// bytes (wallet + every intent + every proposal) and writes a TS fixture used
// by /wallet/demo-wallet on the live site.
//
// Run from dashboard/:
//   node scripts/snapshot-demo-wallet.mjs
//
// Source wallet on localnet:
//   DNY6KLK6jb6dV95KhRKHZjxh1EPyBUZr63yv7cMtMeGf  (name: "treasury")

import { Connection, PublicKey } from '@solana/web3.js';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const RPC = 'http://127.0.0.1:8899';
const PROGRAM_ID = new PublicKey('LUC5TbUhLpT2dZuC2qA4vMZdxJXsbcsUVejTqLJBJWR');
const SOURCE_WALLET = new PublicKey('DNY6KLK6jb6dV95KhRKHZjxh1EPyBUZr63yv7cMtMeGf');

const INTENT_SEED = Buffer.from('intent');
const PROPOSAL_SEED = Buffer.from('proposal');
const PREFIX_LEN = 2;
const DISC_PROPOSAL = 4;

function findIntentPDA(wallet, index) {
  return PublicKey.findProgramAddressSync(
    [INTENT_SEED, wallet.toBuffer(), Buffer.from([index])],
    PROGRAM_ID,
  );
}

const conn = new Connection(RPC, 'confirmed');

const walletAcc = await conn.getAccountInfo(SOURCE_WALLET);
if (!walletAcc) throw new Error('treasury wallet not on localnet');

const walletData = walletAcc.data;
const intentCount = walletData[PREFIX_LEN + 8];
const proposalIndex = walletData.readBigUInt64LE(PREFIX_LEN);
console.log(`treasury: ${intentCount} intents, proposalIndex=${proposalIndex}`);

const intents = [];
for (let i = 0; i < intentCount; i++) {
  const [pda] = findIntentPDA(SOURCE_WALLET, i);
  const info = await conn.getAccountInfo(pda);
  if (!info) {
    console.warn(`  intent ${i}: missing on-chain`);
    continue;
  }
  intents.push({ index: i, address: pda.toBase58(), data: info.data.toString('base64') });
  console.log(`  intent ${i}: ${pda.toBase58()} (${info.data.length} bytes)`);
}

// Proposals — scan all program proposals, keep ones for our wallet
const proposalAccs = await conn.getProgramAccounts(PROGRAM_ID, {
  filters: [{ memcmp: { offset: 0, bytes: '5' } }], // base58('[4]') = '5'
});
const proposals = [];
for (const { pubkey, account } of proposalAccs) {
  if (account.data[0] !== DISC_PROPOSAL) continue;
  const walletField = new PublicKey(account.data.subarray(PREFIX_LEN, PREFIX_LEN + 32));
  if (!walletField.equals(SOURCE_WALLET)) continue;
  proposals.push({ address: pubkey.toBase58(), data: account.data.toString('base64') });
  console.log(`  proposal ${pubkey.toBase58()} (${account.data.length} bytes)`);
}

// Emit TypeScript fixture
const out = `// AUTO-GENERATED by scripts/snapshot-demo-wallet.mjs
// Snapshot of localnet \`treasury\` wallet — served read-only at /wallet/demo-wallet.
// To re-snapshot: run \`node scripts/snapshot-demo-wallet.mjs\` from dashboard/.

export const DEMO_WALLET_NAME = 'demo-wallet';
export const DEMO_WALLET_ADDRESS = '${SOURCE_WALLET.toBase58()}';

export interface SnapshotAccount {
  address: string;
  data: string; // base64
}

export const DEMO_WALLET_SNAPSHOT: {
  wallet: SnapshotAccount;
  intents: Array<SnapshotAccount & { index: number }>;
  proposals: SnapshotAccount[];
} = ${JSON.stringify(
    {
      wallet: { address: SOURCE_WALLET.toBase58(), data: walletAcc.data.toString('base64') },
      intents,
      proposals,
    },
    null,
    2,
  )};
`;

const target = path.resolve(__dirname, '..', 'src', 'fixtures', 'demoWallet.ts');
fs.mkdirSync(path.dirname(target), { recursive: true });
fs.writeFileSync(target, out);
console.log(`\nwrote ${target}`);
